# Type Casting
# The process of converting a variable from one data type to another str(),float(),bool()


name="Ameer Hamza"
age=25
gpa=3.2
is_student=True

print(type(name))
print(type(age))
print(type(gpa))
print(type(is_student))


# type casting

gpa=int(gpa)
age=float(age)
print(type(gpa))
print(type(age))

name=bool(name)
print(name)

